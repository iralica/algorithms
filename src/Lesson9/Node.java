package Lesson9;

public class Node {
    private int data;
    private Node next;

    public Node(int data, Object o) {
        this.data = data;
        this.next = next;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public Node getNext() {
        return next;
    }

    public void setNext() {
        this.next = next;
    }
}
